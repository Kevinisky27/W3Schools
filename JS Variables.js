/* JS Variables 
  Existen 4 formas de declarar variables como lo son de diretente manera
*/

x = 3 // De manera automática 
y = 1
console.log(x-y)

var name = "Oscar Obdulio" // Utilizando la palabra `var`
var lastName = "Ramírez Sandoval"
console.log("El estudiante", name, lastName, "es estudiante de emfermería en Jalapa.")

let color1 = "rojo" // Utilizando la palabra reservada `let`
let color2 = "azul"
console.log("los colores de la casa deben de ser: ", color2, " con ", color1)

const PI = 3.14 // Utilizando la palabra reservada `const`


