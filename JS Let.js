/* Cabe resaltar que anteriormente era utilizando var para definir una variable, pero con las nuevas actualizaciones de JS apartir del 2015 se cambió la parte de `var` a *let* */

var color = 'rojo'
console.log('Color:', color)

let name = 'Kevin Alexander'
let facultad = 'Ingeniería en Sistemas'

console.log("El estudiante ", name, 'de la facultad de',facultad)

